
public class Tupla<T1, T2> {

	
	private T1 s1;
	private T2 s2;
	
	
	
	
	public Tupla(T1 s1, T2 s2) {
		super();
		this.s1 = s1;
		this.s2 = s2;
	}
	public T1 getS1() {
		return s1;
	}
	public void setS1(T1 s1) {
		this.s1 = s1;
	}
	public T2 getS2() {
		return s2;
	}
	public void setS2(T2 s2) {
		this.s2 = s2;
	}
	

	
	
	
	
}
